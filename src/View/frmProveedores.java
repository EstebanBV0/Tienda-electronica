    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       // se lee la información del codigo del proveedor
        String codep = codepr.getText();
        // se consulta el proveedor a apartir del codigo y retorna un objeto proveedor 
        Proveedor prov = (Proveedor) ctlproveedor.Searproveedor(codep);
        if (prov == null) {
            JOptionPane.showMessageDialog(this, "code not found");
        } else {
            // se muestra en el formalario cada uno de los atributos del objeto proveedor 
            textid.setText(prov.getProvid() + "");
            txtNit.setText(prov.getNit());
            txtRazonSocial.setText(prov.getRazonsocial());
            txtTelefono.setText(prov.getTelefono());
            txtCorreo.setText(prov.getEmail());
            txtRepresentante.setText(prov.getReprelegal());
            txtSitioWeb.setText(prov.getLinkweb());
            txtCllCra.setText(prov.getCallec());
            txtBarrio.setText(prov.getBarrio());
            txtNumero.setText(prov.getNumero());
            txtCiudad.setText(prov.getCity());

        }


    }//GEN-LAST:event_btnBuscarActionPerformed

