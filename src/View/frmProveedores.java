    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String codep = codepr.getText(); //recibe los datos que vienen del formulario
        String nittp = txtNit.getText();
        String razonp = txtRazonSocial.getText();
        String telp = txtTelefono.getText();
        String correop = txtCorreo.getText();
        String reprep = txtRepresentante.getText();
        String swebp = txtSitioWeb.getText();
        String callec = txtCllCra.getText();
        String barrio = txtBarrio.getText();
        String numero = txtNumero.getText();
        String city = txtCiudad.getText();
        try {
            // se leen todos los datos del formulario y se actualiza
            int proId = Integer.parseInt(textid.getText());
            // se crea el nuevo objeto prov con los datos del proveedor
            
           Proveedor prov = new Proveedor(proId, codep, nittp, razonp, telp, correop, reprep, swebp, callec, barrio, numero, city);
           // se envia el objeto proveedor y se envia al método que retorna true o false en caso de no poder actualizar respectivamente
           boolean response = ctlproveedor.Editproveedor(prov);
            if (response) {
                JOptionPane.showMessageDialog(this, "Se ha completado la actualización");
            } else {
                JOptionPane.showMessageDialog(this, "no se pudo actualizar la información");

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tiene que insertar el codigo para actualizar");
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            int proId = Integer.parseInt(textid.getText());

            Proveedor prove = (Proveedor) ctlproveedor.Deleteproveedor(proId);

            if (prove == null) {
                JOptionPane.showMessageDialog(this, "Se eliminó con Éxito el proveedor");
                codepr.setText("");
                txtNit.setText("");
                txtRazonSocial.setText("");
                txtTelefono.setText("");
                txtCorreo.setText("");
                txtRepresentante.setText("");
                txtSitioWeb.setText("");
                txtCllCra.setText("");
                txtBarrio.setText("");
                txtNumero.setText("");
                txtCiudad.setText("");
                textid.setText("");

            } else {
                JOptionPane.showMessageDialog(this, "Tiene que insertar el ID correspondiente");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tiene que insertar el ID correspondiente");
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

